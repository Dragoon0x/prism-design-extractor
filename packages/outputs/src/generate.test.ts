describe('asset-bundle-zip', () => {
  it('returns a ZIP with manifest.json', async () => {
    const artifact = await generate(fixtureExtraction(), 'asset-bundle-zip');
    if (artifact.content.kind !== 'bytes') throw new Error('expected bytes');
    const zip = await JSZip.loadAsync(Buffer.from(artifact.content.bytesBase64, 'base64'));
    const manifest = await zip.file('manifest.json')?.async('string');
    expect(manifest).toBeDefined();
    const parsed = JSON.parse(manifest!) as { assetCount: number; assets: unknown[] };
    expect(parsed.assetCount).toBe(0); // fixture has no assets
  });
});

