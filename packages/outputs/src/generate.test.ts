import JSZip from 'jszip';
import { describe, expect, it } from 'vitest';
import { fixtureExtraction } from './fixture.js';
import { generate, generateAll, SUPPORTED_FORMATS_V1 } from './index.js';

describe('generate()', () => {
  it('produces an Artifact for every supported format', async () => {
    const extraction = fixtureExtraction();
    for (const format of SUPPORTED_FORMATS_V1) {
      const artifact = await generate(extraction, format);
      expect(artifact.format).toBe(format);
      expect(artifact.hash).toMatch(/^[0-9a-f]{64}$/);
      expect(artifact.sizeBytes).toBeGreaterThan(0);
      expect(['text', 'bytes']).toContain(artifact.content.kind);
    }
  });

  it('generateAll() returns one artifact per supported format', async () => {
    const artifacts = await generateAll(fixtureExtraction());
    expect(artifacts).toHaveLength(SUPPORTED_FORMATS_V1.length);
  });
});

describe('design-tokens-json', () => {
  it('produces valid DTCG with semantic roles as leaf keys', async () => {
    const artifact = await generate(fixtureExtraction(), 'design-tokens-json');
    if (artifact.content.kind !== 'text') throw new Error('expected text');
    const tree = JSON.parse(artifact.content.text) as Record<string, unknown>;
    expect(tree.color).toBeDefined();
    const colorGroup = tree.color as Record<string, { $value: string; $type: string }>;
    expect(colorGroup.primary?.$value).toBe('#3b82f6');
    expect(colorGroup.primary?.$type).toBe('color');
    const spacingGroup = tree.spacing as Record<string, { $value: string }>;
    expect(spacingGroup['space-16']?.$value).toBe('16px');
  });
});

