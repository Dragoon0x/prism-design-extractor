describe('figma-tokens-json', () => {
  it('emits Tokens Studio-shaped tree', async () => {
    const artifact = await generate(fixtureExtraction(), 'figma-tokens-json');
    if (artifact.content.kind !== 'text') throw new Error('expected text');
    const tree = JSON.parse(artifact.content.text) as {
      global: Record<string, Record<string, { value: unknown; type: string }>>;
      $themes: unknown[];
      $metadata: { tokenSetOrder: string[] };
    };
    expect(tree.$metadata.tokenSetOrder).toEqual(['global']);
    expect(tree.global.color?.primary?.value).toBe('#3b82f6');
    expect(tree.global.color?.primary?.type).toBe('color');
    expect(tree.global.spacing?.['space-16']?.value).toBe('16');
    expect(tree.global.spacing?.['space-16']?.type).toBe('spacing');
  });
});

